export const ELEMENTS: ChemicalElement[] = Array.from({ length: 118 }, (_, i) => createBaseElement(i + 1));

export const getElementByPosition = (p: number, g: number): ChemicalElement | undefined => {
    return ELEMENTS.find(el => {
        const pos = getPosition(el.number);
        return pos.p === p && pos.g === g;
    });
};

const populate = (n: number, data: Partial<ChemicalElement>) => {
    const idx = n - 1;
    if (ELEMENTS[idx]) ELEMENTS[idx] = { ...ELEMENTS[idx], ...data };
};

