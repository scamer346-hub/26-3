// Helper function to determine the visual grid position for a standard periodic table layout
const getPosition = (n: number): { p: number, g: number } => {
    if (n === 1) return { p: 1, g: 1 }; // H
    if (n === 2) return { p: 1, g: 18 }; // He

    if (n >= 3 && n <= 4) return { p: 2, g: n - 2 }; // Li, Be
    if (n >= 5 && n <= 10) return { p: 2, g: n + 8 }; // B-Ne

    if (n >= 11 && n <= 12) return { p: 3, g: n - 10 }; // Na, Mg
    if (n >= 13 && n <= 18) return { p: 3, g: n }; // Al-Ar

    if (n >= 19 && n <= 36) return { p: 4, g: n - 18 }; // K-Kr
    if (n >= 37 && n <= 54) return { p: 5, g: n - 36 }; // Rb-Xe

    if (n >= 55 && n <= 56) return { p: 6, g: n - 54 }; // Cs, Ba
    if (n === 57) return { p: 6, g: 3 }; // Lanthanum
    if (n >= 58 && n <= 71) return { p: 9, g: n - 58 + 4 }; // Lanthanides series below
    if (n >= 72 && n <= 86) return { p: 6, g: n - 68 }; // Hf-Rn

    if (n >= 87 && n <= 88) return { p: 7, g: n - 86 }; // Fr, Ra
    if (n === 89) return { p: 7, g: 3 }; // Actinium
    if (n >= 90 && n <= 103) return { p: 10, g: n - 90 + 4 }; // Actinides series below
    if (n >= 104 && n <= 118) return { p: 7, g: n - 100 }; // Rf-Og

    return { p: -1, g: -1 };
};

const getCategory = (n: number): ElementCategory => {
    if ([1, 6, 7, 8, 15, 16, 34].includes(n)) return 'phi-kim';
    if ([2, 10, 18, 36, 54, 86, 118].includes(n)) return 'khi-hiem';
    if ([3, 11, 19, 37, 55, 87].includes(n)) return 'kim-loai-kiem';
    if ([4, 12, 20, 38, 56, 88].includes(n)) return 'kim-loai-kiem-tho';
    if ([5, 14, 32, 33, 51, 52, 84].includes(n)) return 'ban-kim';
    if ([9, 17, 35, 53, 85, 117].includes(n)) return 'halogen';
    if (n >= 58 && n <= 71) return 'lanthanide'; // Note: La is transition metal
    if (n >= 90 && n <= 103) return 'actinide'; // Note: Ac is transition metal
    if ([13, 31, 49, 50, 81, 82, 83, 113, 114, 115, 116].includes(n)) return 'kim-loai-yeu';
    return 'kim-loai-chuyen-tiep'; // Includes La (57) and Ac (89)
};

